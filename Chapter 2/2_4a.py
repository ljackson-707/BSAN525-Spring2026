# Addition
print(8 + 10)

# Subtraction
print(8 - 10)

# Multiplicatin
print(8 * 2)

# Exponentiation
print (8 ** 2)

# Division - produces a float
print(64/8)

# Exact division - produces a float
print(65/8)

# Quotient - divides, but produces an integer
print(65//8)

# Remainder or Modulus
print(65 % 8)

# For multi-line expressions, use a backslash \
print(3 + 4 * \
      2 ** 5)

# Mixed-mode arithmetic invovles integers
# and floating-point numbers
print(3.14 * 3 ** 2)