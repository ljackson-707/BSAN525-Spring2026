# Selection statements allow a computer
# to make choices based on a condition.

# Before you can test conditions in a Python program,
# you need to understand the Boolean data type.
# The two Boolean values are True and False.

# The condition in a selection statement often takes the form of a comparison.
# The result of the comparison is a Boolean value (True or False).
print(4 == 4)     # the == operator tests for equality. this is True.
print(4 != 4)     # False
print(4 < 5)      # True
print(4 >= 3)     # True
print("A" < "B")  # True