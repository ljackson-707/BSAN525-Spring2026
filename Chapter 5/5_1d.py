# To print the contents of a list without brackets and commas,
# you can use a for loop.

for number in [1, 2, 3, 4]:
    print(number, end=" ")      # end parameter is a \n, goes to next line, can modify and print something else at end
# Returns 1 2 3 4

for number in [1, 2, 3, 4]:
    print(number)
# Returns 1 2 3 4 each on separate line    
