"""
A list is a sequence of data values called items or elements.
An item can be of any type.
In Python, a list literal is written as a sequence of data
values separated by commas.
Literal means the way the data looks to us, data not stored
in a variable.
The entire sequence is enclosed in square brackets []
"""

[1951, 1969, 1984]                  # A list of integers
["apples", "oranges", "cherries"]   # A list of strings
